sub table
{
	print "<table id='ResultsTable'>\n";
	print "<tr>\n";
	print "    <th><a href='http://en.wikipedia.org/wiki/United_Kingdom_constituencies'>Constituency</a></th>\n";
	print "    <th><a href='http://en.wikipedia.org/wiki/List_of_political_parties_in_the_United_Kingdom'>Party</a></th>\n";
	print "</tr>\n";
	print "</thead>\n";
	print "<tbody>\n";
	foreach my $m (keys(%map)) { 
		print "<tr>\n";
		print "    <td>". $map{$m}->{title} ."</td>\n";
		print "    <td>". $map{$m}->{party}."</td>\n";
		print "</tr>\n";
	}
	print "</tbody>\n";
	print "</table>\n";
}

# take the map file and put in the seat names and class names ..
{
	while (<STDIN>) {
		chomp;
		$seat = $_;
		$seat =~ s/^.*(seat-[0-9]+).*$/$1/;
		if ($seat =~ /seat/) {
			$name = $map->{$seat}->{title};
			$_ =~ s/seat-\d+/$name/;
		}
		if (/\/body/) {
			table();
		}
		print;
		print "\n";
	}
}
